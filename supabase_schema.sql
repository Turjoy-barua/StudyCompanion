create table if not exists study_sessions (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete cascade default auth.uid(),
  subject text not null,
  duration_minutes integer not null check (duration_minutes > 0),
  session_date date not null,
  created_at timestamptz not null default now()
);

create table if not exists academic_items (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete cascade default auth.uid(),
  title text not null,
  item_kind text not null check (item_kind in ('exam', 'project')),
  exam_type text,
  chapters text,
  importance text not null check (importance in ('critical', 'high', 'medium', 'low')),
  confidence_percent integer not null check (confidence_percent between 0 and 100),
  due_date date not null,
  created_at timestamptz not null default now()
);

create table if not exists study_subjects (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete cascade default auth.uid(),
  name text not null,
  priority text not null check (priority in ('critical', 'high', 'medium', 'low')),
  confidence_percent integer not null check (confidence_percent between 0 and 100),
  weekly_goal_minutes integer not null default 0 check (weekly_goal_minutes >= 0),
  notes text,
  created_at timestamptz not null default now(),
  unique (user_id, name)
);

alter table study_sessions
  add column if not exists user_id uuid references auth.users(id) on delete cascade default auth.uid();

alter table academic_items
  add column if not exists user_id uuid references auth.users(id) on delete cascade default auth.uid();

alter table academic_items
  add column if not exists chapters text;

alter table study_subjects
  add column if not exists user_id uuid references auth.users(id) on delete cascade default auth.uid();

alter table study_subjects
  add column if not exists notes text;

create index if not exists study_sessions_user_date_idx
  on study_sessions (user_id, session_date desc, created_at desc);

create index if not exists academic_items_user_due_idx
  on academic_items (user_id, due_date asc, created_at asc);

create index if not exists study_subjects_user_created_idx
  on study_subjects (user_id, created_at asc);

alter table study_sessions enable row level security;
alter table academic_items enable row level security;
alter table study_subjects enable row level security;

drop policy if exists "study_sessions_select_own" on study_sessions;
create policy "study_sessions_select_own"
on study_sessions for select
using (auth.uid() = user_id);

